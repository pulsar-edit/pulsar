const crypto = require("crypto");
const path = require("path");
const fs = require("fs-plus");
const { UI_VARIABLES, UI_VARIABLES_EXTENDED, SYNTAX_VARIABLES } = require("./theme-variables");

// Generates the Less compatibility shim for modern ("dual") themes.
//
// A dual theme defines its palette as CSS custom properties in
// styles/variables.css. Community packages, community legacy themes, and the
// user stylesheet still compile through Less and expect the classic
// `@import "ui-variables";` contract with concrete color values (so Less color
// functions like darken() keep working). This module statically parses the
// theme's :root palette and writes generated ui-variables.less /
// syntax-variables.less files into a per-theme directory that the theme
// manager puts on the Less import path.

// Parse `--name: value;` declarations out of every :root block in the given
// CSS text. Tolerant, regex-based — the palette file is required to be a flat
// list of custom properties with concrete values (see docs), not arbitrary CSS.
function parseRootCustomProperties(cssText) {
  const properties = new Map();
  const withoutComments = cssText.replace(/\/\*[\s\S]*?\*\//g, "");
  const rootBlockRegex = /:root\s*\{([^}]*)\}/g;
  let blockMatch;
  while ((blockMatch = rootBlockRegex.exec(withoutComments)) !== null) {
    const declarationRegex = /--([\w-]+)\s*:\s*([^;]+);/g;
    let declaration;
    while ((declaration = declarationRegex.exec(blockMatch[1])) !== null) {
      properties.set(declaration[1], declaration[2].trim().replace(/\s+/g, " "));
    }
  }
  return properties;
}

// Substitute var(--x) references with values defined in the same palette, so
// alias entries like `--text-color-selected: var(--text-color-highlight);`
// yield concrete values on the Less side. Bounded depth guards against cycles.
function resolveVarReferences(properties) {
  const resolved = new Map(properties);
  const varRegex = /var\(\s*--([\w-]+)\s*(?:,\s*([^)]*))?\)/g;
  for (const [name, value] of resolved) {
    let current = value;
    for (let depth = 0; depth < 10 && current.includes("var("); depth++) {
      current = current.replace(varRegex, (match, refName, fallback) => {
        if (resolved.has(refName)) return resolved.get(refName);
        if (fallback != null) return fallback.trim();
        return match;
      });
    }
    resolved.set(name, current);
  }
  return resolved;
}

// A value is only usable on the Less side when it resolved to something
// concrete. Unresolved var() references or CSS-only color functions would
// break Less color functions applied to them by community stylesheets, so
// those entries are omitted and the static fallback values apply instead.
function isConcreteValue(value) {
  return !/var\(|color-mix\(|\bfrom\b|light-dark\(/.test(value);
}

function generateLessSource(fallbackImport, variableNames, properties) {
  const lines = [
    "// Generated by Lumine from the active theme's CSS custom properties.",
    "// Do not edit — regenerated on every theme change.",
    `@import "${fallbackImport}";`,
    "",
  ];
  for (const name of variableNames) {
    const value = properties.get(name);
    if (value != null && isConcreteValue(value)) {
      lines.push(`@${name}: ${value};`);
    }
  }
  return lines.join("\n") + "\n";
}

function generateShimSources(properties, { includeUi = true, includeSyntax = true } = {}) {
  const resolved = resolveVarReferences(properties);
  const sources = {};
  if (includeUi) {
    sources["ui-variables.less"] = generateLessSource(
      "variables/ui-variables",
      [...UI_VARIABLES, ...UI_VARIABLES_EXTENDED],
      resolved,
    );
  }
  if (includeSyntax) {
    sources["syntax-variables.less"] = generateLessSource(
      "variables/syntax-variables",
      SYNTAX_VARIABLES,
      resolved,
    );
  }
  return sources;
}

// Write the shim files for the given theme set into
// `<ATOM_HOME>/compile-cache/theme-shims/<label>-<hash>/` and prune shim
// directories left over from previous palette versions of the same label.
// Only the sides provided by modern themes are written (a legacy theme in the
// pair provides its own Less variables file, which precedes the shim on the
// import path anyway). The content hash in the directory name doubles as the
// cache key: a palette change changes the import-path list, which invalidates
// less-cache's imports-derived cache directory.
function writeShimDirectory(label, paletteCssText, options = {}) {
  const properties = parseRootCustomProperties(paletteCssText);
  const sources = generateShimSources(properties, options);
  const contents = Object.values(sources).join("");
  const hash = crypto.createHash("sha1").update(contents).digest("hex").slice(0, 12);

  const shimsRoot = path.join(process.env.ATOM_HOME, "compile-cache", "theme-shims");
  const shimDir = path.join(shimsRoot, `${label}-${hash}`);

  if (!fs.isDirectorySync(shimDir)) {
    fs.makeTreeSync(shimDir);
    for (const [fileName, source] of Object.entries(sources)) {
      fs.writeFileSync(path.join(shimDir, fileName), source);
    }
  }

  // Prune stale shim directories for this theme set.
  if (fs.isDirectorySync(shimsRoot)) {
    for (const entry of fs.readdirSync(shimsRoot)) {
      if (entry.startsWith(`${label}-`) && entry !== `${label}-${hash}`) {
        try {
          fs.removeSync(path.join(shimsRoot, entry));
        } catch {
          // A locked directory is only stale disk space; ignore.
        }
      }
    }
  }

  return shimDir;
}

module.exports = {
  parseRootCustomProperties,
  resolveVarReferences,
  generateShimSources,
  writeShimDirectory,
};
